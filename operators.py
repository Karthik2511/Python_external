# Arithmetic Operators
a, b = 10, 5
print("Arithmetic Operators:")
print(f"{a} + {b} = {a + b}")  # Addition
print(f"{a} - {b} = {a - b}")  # Subtraction
print(f"{a} * {b} = {a * b}")  # Multiplication
print(f"{a} / {b} = {a / b}")  # Division

# Comparison Operators
print("\nComparison Operators:")
print(f"{a} > {b} = {a > b}")  # Greater than
print(f"{a} < {b} = {a < b}")  # Less than
print(f"{a} == {b} = {a == b}")  # Equal to

# Logical Operators
x, y = True, False
print("\nLogical Operators:")
print(f"{x} and {y} = {x and y}")  # AND
print(f"{x} or {y} = {x or y}")    # OR
print(f"not {x} = {not x}")        # NOT

# Assignment Operator
c = 5
print("\nAssignment Operator:")
c += 3  # c = c + 3
print(f"c += 3 -> c = {c}")

# Bitwise Operators
print("\nBitwise Operators:")
print(f"10 & 5 = {10 & 5}")  # AND
print(f"10 | 5 = {10 | 5}")  # OR

# Membership Operator
list_example = [1, 2, 3, 4, 5]
print("\nMembership Operators:")
print(f"3 in {list_example} = {3 in list_example}")  # in
print(f"6 not in {list_example} = {6 not in list_example}")  # not in
